#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int	a;
	char			nb;

	a = n;
	if (n < 0)
	{
		a = n * -1;
		write(fd, "-", 1);
	}
	if (a >= 10)
	{
		ft_putnbr_fd(a / 10, fd);
	}
	nb = a % 10 + 48;
	write(fd, &nb, 1);
}
